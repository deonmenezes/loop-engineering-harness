def big_banner(text: str) -> list[str]:
    """Return 5 rows of block-letter art for `text` (unknown chars skipped)."""
    glyphs = [_F[c] for c in text.lower() if c in _F]
    if not glyphs:
        return []
    return ["  ".join(g[r] for g in glyphs) for r in range(5)]


